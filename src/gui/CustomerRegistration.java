    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String mobile = jTextField2.getText();

        String fn = jTextField1.getText();
        String nic = jTextField3.getText();
        String email = jTextField4.getText();

        if (mobile.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter your mobile number", "Warning", JOptionPane.WARNING_MESSAGE);

        } else if (!mobile.matches("^07[01245678]{1}[0-9]{7}$")) {

            JOptionPane.showMessageDialog(this, "Please enter a valid mobile number", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (fn.isEmpty()) {

            JOptionPane.showMessageDialog(this, "Please enter your first name", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (nic.isEmpty()) {

            JOptionPane.showMessageDialog(this, "Please enter your NIC", "Warning", JOptionPane.WARNING_MESSAGE);

        } else if (jTextField4.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter your email", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (!jTextField4.getText().matches("^(?=.{1,64}@)[A-Za-z0-9\\+_-]+(\\.[A-Za-z0-9\\+_-]+)*@[^-][A-Za-z0-9\\+-]+(\\.[A-Za-z0-9\\+-]+)*(\\.[A-Za-z]{2,})$")) {
            JOptionPane.showMessageDialog(this, "Invalid email address", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {

            try {

                ResultSet resultSet = MySQL2.executeSearch("SELECT * FROM `customer` WHERE `mobile`='" + mobile + "' AND `NIC`='" + nic + "' ");

                if (resultSet.next()) {
                    JOptionPane.showMessageDialog(this, "Customer already registered", "Warning", JOptionPane.WARNING_MESSAGE);
                } else {
                    MySQL2.executeIUD("INSERT INTO `customer` (`NIC`,`mobile`,`fname`,`points`,`email`) VALUES "
                            + "('" + nic + "','" + mobile + "','" + fn + "','0','" + email + "')");

                    logger.log(Level.INFO, "New Customer account created:{0}", mobile);

                    loadCustomers(jTextField5.getText());
                    reset();
                }

            } catch (Exception e) {
                logger.log(Level.SEVERE, "New Customer account creating error", e);
                e.printStackTrace();
            }

        }

    }//GEN-LAST:event_jButton1ActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        int row = jTable1.getSelectedRow();

        jTextField2.setText(String.valueOf(jTable1.getValueAt(row, 1)));

        jTextField3.setText(String.valueOf(jTable1.getValueAt(row, 0)));
        jTextField3.setEditable(false);
        jTextField1.setText(String.valueOf(jTable1.getValueAt(row, 2)));
        jTextField4.setText(String.valueOf(jTable1.getValueAt(row, 4)));

        jButton1.setEnabled(false);

        if (evt.getClickCount() == 2) {
            if (invoice != null) {

                invoice.getjTextField4().setText(String.valueOf(jTable1.getValueAt(row, 0)));
                // invoice.getjLabel6().setText(String.valueOf(jTable1.getValueAt(row, 3)));
                invoice.updateOriginalPoint(String.valueOf(jTable1.getValueAt(row, 3)));
                invoice.accessCalculate();

                //    invoice.getjTextField4().setText(String.valueOf(jTable1.getValueAt(row, 4)));
                SwingUtilities.getWindowAncestor(this).dispose();
            }
        }

    }//GEN-LAST:event_jTable1MouseClicked

