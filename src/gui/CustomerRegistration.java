    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        int row = jTable1.getSelectedRow();

        jButton1.setEnabled(false);

        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Please select a row to update", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {

            String mobile = jTextField2.getText();
            String fn = jTextField1.getText();
            String nic = jTextField3.getText();
            String email = jTextField4.getText();

            if ( mobile.equals(String.valueOf(jTable1.getValueAt(row, 1))) && fn.equals(String.valueOf(jTable1.getValueAt(row, 2))) && email.equals(String.valueOf(jTable1.getValueAt(row, 4))) ) {
                JOptionPane.showMessageDialog(this, "Please update data before clicking update", "Warning", JOptionPane.WARNING_MESSAGE);

            } else {

                try {

                    ResultSet resultSet = MySQL2.executeSearch("SELECT * FROM `customer` WHERE `nic`='" + nic + "' AND `mobile`='" + mobile + "' ");
                    boolean canupdate = false;
                    if (resultSet.next()) {

                        if (!resultSet.getString("email").equals(email)) {
                            JOptionPane.showMessageDialog(this, "Customer already registered", "Warning", JOptionPane.WARNING_MESSAGE);

                        } else {
                            canupdate = true;

                        }

                    } else {

                        canupdate = true;

                    }

                    if (canupdate) {

                        MySQL2.executeIUD("UPDATE `customer` SET `fname`='" + fn + "',`mobile`='" + mobile + "' WHERE `NIC`='" + nic + "' ");
                        logger.log(Level.INFO, "Customer account updated:{0}", mobile);
                        loadCustomers();
                        reset();
                    }

                } catch (Exception e) {
                    logger.log(Level.SEVERE, "Customer account update error", e);
                    e.printStackTrace();
                }

            }

        }
    }//GEN-LAST:event_jButton2ActionPerformed

