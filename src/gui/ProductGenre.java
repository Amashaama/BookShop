    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String updatedGenre = jTextField1.getText();
        int row = jTable1.getSelectedRow();
        
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Please select a row", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {
            
            try {
                ResultSet rs = MySQL2.executeSearch("SELECT * FROM `genre` WHERE `id`='" + String.valueOf(jTable1.getValueAt(row, 0)) + "'");
                
                if (rs.next()) {
                    if (updatedGenre.equals(String.valueOf(jTable1.getValueAt(row, 1)))) {
                        JOptionPane.showMessageDialog(this, "Please change genre name before update", "Warning", JOptionPane.WARNING_MESSAGE);
                    } else {
                        logger.log(Level.INFO, "Update Genre:{0}", updatedGenre);
                        MySQL2.executeIUD("UPDATE `genre` SET `name`='" + updatedGenre + "' WHERE `id`='" + rs.getString("id") + "' ");
                        loadGenre();
                        loadProGenre();
                        clearAll();
                    }
                }
                
            } catch (Exception e) {
                logger.log(Level.SEVERE, "Error in Updating a genre", e);
                e.printStackTrace();
            }
        }
        

    }//GEN-LAST:event_jButton2ActionPerformed

