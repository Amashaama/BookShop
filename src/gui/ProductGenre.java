    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            String typeGenre = jTextField1.getText();
            String selectGenre = String.valueOf(jComboBox1.getSelectedItem());
            
            if (selectGenre.equals("Select") && typeGenre.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please select a genre or type a new one", "Warning", JOptionPane.WARNING_MESSAGE);
            } else if (!selectGenre.equals("Select") && !typeGenre.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please add one genre at a time", "Warning", JOptionPane.WARNING_MESSAGE);
            } else if (!selectGenre.equals("Select") && typeGenre.isEmpty()) {
                
                ResultSet rs = MySQL2.executeSearch("SELECT * FROM `product_has_genre` WHERE `product_id`='" + this.id + "' AND `genre_id`='" + genMap.get(selectGenre) + "' ");
                
                if (rs.next()) {
                    JOptionPane.showMessageDialog(this, "this genre already added to the product", "Warning", JOptionPane.WARNING_MESSAGE);
                } else {
                    logger.log(Level.INFO, "Add Genre:{0}", selectGenre);
                    MySQL2.executeIUD("INSERT INTO `product_has_genre` VALUES ('" + this.id + "','" + genMap.get(selectGenre) + "')");
                    loadProGenre();
                    clearAll();
                }
                
            } else if (selectGenre.equals("Select") && !typeGenre.isEmpty()) {
                
                ResultSet rs2 = MySQL2.executeSearch("SELECT * FROM `genre` WHERE `name`='" + typeGenre + "'");
                
                if (rs2.next()) {
                    JOptionPane.showMessageDialog(this, "this genre already exist", "Warning", JOptionPane.WARNING_MESSAGE);
                } else {
                    
                    ResultSet rs = MySQL2.executeSearch("SELECT * FROM `product_has_genre` WHERE `product_id`='" + this.id + "' AND `genre_id`='" + genMap.get(typeGenre) + "' ");
                    
                    if (rs.next()) {
                        JOptionPane.showMessageDialog(this, "this genre already added to the product", "Warning", JOptionPane.WARNING_MESSAGE);
                    } else {
                        logger.log(Level.INFO, "Add new Genre:{0}", selectGenre);
                        MySQL2.executeIUD("INSERT INTO `genre` (`name`) VALUES('" + typeGenre + "') ");
                        loadGenre();
                        MySQL2.executeIUD("INSERT INTO `product_has_genre` VALUES ('" + this.id + "','" + genMap.get(typeGenre) + "')");
                        loadProGenre();
                        
                        clearAll();
                    }
                    
                }
                
            }
        } catch (Exception e) {
            logger.log(Level.SEVERE, "Error in adding a genre", e);
            e.printStackTrace();
        }
        

    }//GEN-LAST:event_jButton1ActionPerformed

