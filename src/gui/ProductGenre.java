    private void loadGenre() {
        
        try {
            
            ResultSet rs = MySQL2.executeSearch("SELECT * FROM `genre`");
            
            Vector<String> vector = new Vector<>();
            vector.add("Select");
            
            while (rs.next()) {
                vector.add(rs.getString("name"));
                genMap.put(rs.getString("name"), rs.getString("id"));
                
            }
            
            DefaultComboBoxModel dcm = new DefaultComboBoxModel(vector);
            jComboBox1.setModel(dcm);
            
        } catch (Exception e) {
            logger.log(Level.SEVERE, "Error in loading genres comboBox", e);
            e.printStackTrace();
        }
        
    }
    
    private void loadProGenre() {
        
        try {
            
            ResultSet rs = MySQL2.executeSearch("SELECT * FROM `product_has_genre` INNER JOIN `genre` ON `genre`.`id`=`product_has_genre`.`genre_id` WHERE `product_has_genre`.`product_id`='" + this.id + "' ");
            
            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);
            
            while (rs.next()) {
                Vector<String> vector = new Vector<>();
                vector.add(rs.getString("id"));
                vector.add(rs.getString("name"));
                dtm.addRow(vector);
            }
            
        } catch (Exception e) {
            logger.log(Level.SEVERE, "Error in loading added genre to table", e);
            e.printStackTrace();
        }
        
    }

