    private void initializeEmpLogin() {
       
        String homePath = System.getProperty("user.home");
        String folderPath = homePath + File.separator + "Documents" + File.separator + "sadIndividualApp" + File.separator + "Logs";

        File newFolder = new File(folderPath);
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd-hh-mm-ss");

        if (newFolder.exists()) {
            String fileName = sdf.format(new Date());
            if (handler == null) {
                try {
                    SignIn.handler = new FileHandler();

                    SignIn.handler = new FileHandler(folderPath + "/" + fileName + ".txt");
                    handler.setFormatter(new SimpleFormatter());
                    logger.addHandler(handler);
                    logger.setLevel(Level.ALL);
                    logger.info("Log File Created Successfully!");
                    logger.info("Employee Login Page Accessed");

                } catch (IOException e) {
                    logger.log(Level.WARNING, "Log File Created Unsuccessfully!", e);

                }
            }
            return;
        }

        newFolder.mkdirs();
        System.out.println(folderPath);
    }
    
