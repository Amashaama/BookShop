    private void sendOtp(String toEmail) {
        currentOtp = verificationCodeGenerator(6);

        String fromEmail = "wasanthasanjaya1234@gmail.com";
        String password = "gmlp usso cbpp snzr";
        Properties properties = new Properties();
        properties.put("mail.smtp.auth", "true");
        properties.put("mail.smtp.starttls.enable", "true");
        properties.put("mail.smtp.host", "smtp.gmail.com");
        properties.put("mail.smtp.port", "587");

        Session session = Session.getInstance(properties, new Authenticator() {
            protected PasswordAuthentication getPasswordAuthentication() {
                return new PasswordAuthentication(fromEmail, password);
            }
        });

        try {

            StringBuilder emailBody = new StringBuilder();

            emailBody.append("<h1 style='color: #2E86C1;'>ABC Book Shop OTP Code</h1>")
                    .append("<p>Dear Customer,</p>")
                    .append("<p>Please find the OTP for your Log In below:</p>");
            emailBody.append("Your OTP for LogIn is: " + currentOtp);

            Message message = new MimeMessage(session);
            message.setFrom(new InternetAddress(fromEmail));
            message.setRecipients(Message.RecipientType.TO, InternetAddress.parse(toEmail));
            message.setSubject("Your OTP For ABC Book Shop App");
            message.setContent(emailBody.toString(), "text/html");

            Transport.send(message);
            JOptionPane.showMessageDialog(this, "OTP sent to " + toEmail, "Information", JOptionPane.INFORMATION_MESSAGE);
        } catch (MessagingException me) {
            me.printStackTrace();
        }
    }

